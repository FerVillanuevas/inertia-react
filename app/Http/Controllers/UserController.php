     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::findOrFail($id);
        $file = $request->file;
        if($request->has('file')) {
            $stored = $file->store('avatar', 's3');
        }


        $user->update([
            'name' => $request->name,
            'avatar' => $stored ? $stored : null
        ]);

        return $user;
    }

